#include "LogisimTunnel.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimTunnel::Init(SInt2 sPosition)
{
	CLogisimComponent::Init(sPosition);
	mszLabel.Init();
	miWidth = 0;
	meFacing = LF_NotSet;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimTunnel::Kill(void)
{
	mszLabel.Kill();
	CLogisimComponent::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimTunnel::SetWidth(int iWidth)
{
	miWidth = iWidth;
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimTunnel::SetLabel(char* szLabel)
{
	mszLabel.Set(szLabel);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimTunnel::SetFacing(ELogisimFacing eFacing)
{
	meFacing = eFacing;
}

