#include "LogisimBuffer.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimBuffer::Init(SInt2 sPosition)
{
	CLogisimFacing::Init(sPosition);
	mszLabel.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimBuffer::Kill(void)
{
	mszLabel.Kill();
	CLogisimFacing::Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimBuffer::SetLabel(char* szLabel)
{
	mszLabel.Set(szLabel);
}

