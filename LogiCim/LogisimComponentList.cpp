#include "BaseLib/ConstructorCall.h"
#include "LogisimComponentList.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimComponentList::Init(void)
{
	mcList.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimComponentList::Kill(void)
{
	CLogisimComponent*	pcComponent;
	int					iNum;
	int					i;

	iNum = mcList.NumElements();
	for (i = 0; i < iNum; i++)
	{
		pcComponent = (CLogisimComponent*)mcList.Get(i);
		pcComponent->Kill();
	}
	mcList.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CLogisimPullResistor* CLogisimComponentList::CreatePullResistor(void)
{
	return New<CLogisimPullResistor>((CLogisimPullResistor*)mcList.Add(sizeof(CLogisimPullResistor)));
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CLogisimTunnel* CLogisimComponentList::CreateTunnel(void)
{
	return New<CLogisimTunnel>((CLogisimTunnel*)mcList.Add(sizeof(CLogisimTunnel)));
}

