void CLogisimLibrary::Init(char* szName, char* szDescription)
{
	mszName.Init(szName);
	mszDescription.Init(szDescription);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimLibrary::Kill(void)
{
	mszDescription.Kill();
	mszName.Kill();
}

