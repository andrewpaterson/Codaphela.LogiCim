#include "LogisimCircuit.h"


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimCircuit::Init(char* szName)
{
	mszName.Init(szName);

	mlWires.Init();
	mapComponents.Init();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimCircuit::Kill(void)
{
	int				iNum;
	int				i;
	CLogisimWire*	pcWire;

	mapComponents.Kill();

	iNum = mlWires.NumElements();
	for (i = 0; i < iNum; i++)
	{
		pcWire = mlWires.Get(i);
		pcWire->Kill();
	}
	mlWires.Kill();

	mszName.Kill();
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
CLogisimWire* CLogisimCircuit::AddWire(void)
{
	return mlWires.Add();
}



//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
void CLogisimCircuit::AddComponent(CLogisimComponent* pcComponent)
{
	mapComponents.Add(pcComponent);
}


//////////////////////////////////////////////////////////////////////////
//
//
//////////////////////////////////////////////////////////////////////////
char* CLogisimCircuit::GetName(void)
{
	return mszName.Text();
}

